def main():
    parser = argparse.ArgumentParser(description='Generate detailed PPT outline')
    parser.add_argument('topic', help='PPT主题')
    parser.add_argument('--pages', '-p', type=int, default=16, help='页数（默认16页）')
    parser.add_argument('--output', '-o', help='输出文件路径')
    
    args = parser.parse_args()
    
    generator = OutlineGenerator()
    outline = generator.generate_business_proposal(args.topic, args.pages)
    
    output_file = args.output or f"{args.topic}_PPT详细大纲.md"
    Path(output_file).write_text(outline, encoding='utf-8')
    
    print(f"✅ 详细大纲已生成：{output_file}")
    print(f"📄 模板类型：商务提案")
    print(f"📊 页数：{args.pages}页")
    print(f"\n💡 大纲特点：")
    print("  • 每页都有【设计建议】")
    print("  • 【内容板块】结构清晰")
    print("  • 【配图建议】指导视觉设计")
    print("  • 可直接用于PPT制作")


if __name__ == '__main__':
    main()
